/*Problem 07: Substraction*/

public class Problem07 {
    public static void main(String[] args) {
     
		// Create integer named number_a with value 453245        
        int number_a = 453245;

        // Create integer named number_b with value 23    
        int number_b = 23;

        // Create empty float number_c
        float number_c;

        // Substract number b from number a and store the result of the operation in number_c
        number_c = number_a - number_b;

		//Print the description of the operation, along with the result      
        System.out.println("the substraction of "+ number_a + " and " + number_b + " is " + number_c);
                
    }
}